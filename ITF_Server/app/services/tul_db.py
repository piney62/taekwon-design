def build_prompt(tul_id: str, movement_no: int, angle_text: str, language: str = "ko") -> str:
    tul = TUL_DB.get(tul_id)
    tul_display = tul["name"] if tul else tul_id
    movements = tul["movements"] if tul else []
    movement = next((m for m in movements if m["no"] == movement_no), None)

    if movement:
        key_points = movement.get("key_points", [])
        stance_val = movement.get("stance", "해당 서기")
        technique_val = movement.get("technique", "해당 기술")
        direction_val = movement.get("direction", "")
        if key_points:
            key_points_text = "\n".join(f"  - {kp}" for kp in key_points)
            standard = f"""
【{tul_display} {movement_no}번 동작의 정확한 기준】
- 동작명:  {movement['name']}
- 서기:    {stance_val}
- 기술:    {technique_val}
- 방향:    {direction_val}
- 반드시 충족해야 할 핵심 기준:
{key_points_text}
"""
        else:
            standard = f"""
【{tul_display} {movement_no}번 동작】
- 동작명:  {movement['name']}
- 서기:    {stance_val}
- 기술:    {technique_val}
"""
    else:
        standard = f"【{tul_display} {movement_no}번 동작】\n(기준 데이터 없음)"
        stance_val = "해당 서기"
        technique_val = "해당 기술"

