# Maps English angle keys → Korean labels used in the AI prompt
_ANGLE_LABELS = {
    "left_elbow":    "왼팔꿈치",
    "right_elbow":   "오른팔꿈치",
    "left_shoulder": "왼어깨",
    "right_shoulder":"오른어깨",
    "left_knee":     "왼무릎",
    "right_knee":    "오른무릎",
    "left_hip":      "왼골반",
    "right_hip":     "오른골반",
}


def build_angle_text(master_angles: dict, student_angles: dict) -> str:
    text = "【관절 각도 데이터】\n사범:\n"
    for k, v in master_angles.items():
        label = _ANGLE_LABELS.get(k, k)
        text += f"  {label}: {v}°\n"
    text += "수련생:\n"
    for k, v in student_angles.items():
        label = _ANGLE_LABELS.get(k, k)
        diff = round(v - master_angles.get(k, v), 1)
        sign = "+" if diff > 0 else ""
        text += f"  {label}: {v}° (차이 {sign}{diff}°)\n"
    return text


